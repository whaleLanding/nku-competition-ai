function autocomplete(inp, arr) {
    var currentFocus;

    function highlightMatch(text, query) {
        query = query.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
        const regex = new RegExp(`(${query})`, 'gi');
        return text.replace(regex, '<span class="highlight">$1</span>');
    }

    inp.addEventListener("input", function(e) {
        var a, val = this.value;
        closeAllLists();
        if (!val) { return false; }
        currentFocus = -1;
        
        a = document.createElement("DIV");
        a.setAttribute("id", this.id + "autocomplete-list");
        a.setAttribute("class", "autocomplete-items");
        this.parentNode.appendChild(a);

        const matches = arr
            .filter(item => item.toLowerCase().includes(val.toLowerCase()))
            .sort((a, b) => {
                const aStartsWith = a.toLowerCase().startsWith(val.toLowerCase());
                const bStartsWith = b.toLowerCase().startsWith(val.toLowerCase());
                if (aStartsWith && !bStartsWith) return -1;
                if (!aStartsWith && bStartsWith) return 1;
                return 0;
            });

        if (matches.length === 0) {
            const noResults = document.createElement("DIV");
            noResults.className = "no-results";
            noResults.textContent = "未找到匹配的结果";
            a.appendChild(noResults);
            return;
        }

        // 只显示前10个结果
        const displayMatches = matches.slice(0, 10);
        displayMatches.forEach(item => {
            const b = document.createElement("DIV");
            b.innerHTML = highlightMatch(item, val);
            b.innerHTML += `<input type='hidden' value='${item}'>`;
            
            b.addEventListener("click", function(e) {
                const keyword = this.getElementsByTagName("input")[0].value;
                const encodedKeyword = encodeURIComponent(keyword);
                window.location.href = `display.html?keyword=${encodedKeyword}`;
            });
            a.appendChild(b);
        });

        // 如果还有更多结果，显示"..."
        if (matches.length > 10) {
            const more = document.createElement("DIV");
            more.className = "more-results";
            more.textContent = `还有 ${matches.length - 10} 个结果`;
            more.style.pointerEvents = "none";
            more.style.color = "gray";
            a.appendChild(more);
        }
    });

    inp.addEventListener("keydown", function(e) {
        var x = document.getElementById(this.id + "autocomplete-list");
        if (x) x = x.getElementsByTagName("div");
        if (e.keyCode == 40) {
            currentFocus++;
            addActive(x);
        } else if (e.keyCode == 38) {
            currentFocus--;
            addActive(x);
        } else if (e.keyCode == 13) {
            e.preventDefault();
            if (currentFocus > -1) {
                if (x && !x[currentFocus].classList.contains('no-results') && 
                    !x[currentFocus].classList.contains('more-results')) {
                    x[currentFocus].click();
                }
            }
        }
    });

    function addActive(x) {
        if (!x || x.length === 0) return false;
        removeActive(x);
        if (currentFocus >= x.length) currentFocus = 0;
        if (currentFocus < 0) currentFocus = (x.length - 1);
        if (!x[currentFocus].classList.contains('no-results') && 
            !x[currentFocus].classList.contains('more-results')) {
            x[currentFocus].classList.add("autocomplete-active");
        }
    }

    function removeActive(x) {
        for (var i = 0; i < x.length; i++) {
            x[i].classList.remove("autocomplete-active");
        }
    }

    function closeAllLists(elmnt) {
        var x = document.getElementsByClassName("autocomplete-items");
        for (var i = 0; i < x.length; i++) {
            if (elmnt != x[i] && elmnt != inp) {
                x[i].parentNode.removeChild(x[i]);
            }
        }
    }

    document.addEventListener("click", function(e) {
        closeAllLists(e.target);
    });
}

